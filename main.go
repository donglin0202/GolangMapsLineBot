package main

import (
	"fmt"
	"log"
	"net/http"
	"os"
	//"strconv"

	"github.com/line/line-bot-sdk-go/linebot"
)

var bot *linebot.Client

func main() {
	var err error
	bot, err = linebot.New(os.Getenv("ChannelSecret"), os.Getenv("ChannelAccessToken"))
	log.Println("Bot:", bot, " err:", err)
	http.HandleFunc("/callback", callbackHandler)
	port := os.Getenv("PORT")
	addr := fmt.Sprintf(":%s", port)
	http.ListenAndServe(addr, nil)
}

func callbackHandler(w http.ResponseWriter, r *http.Request) {
	events, err := bot.ParseRequest(r)

	if err != nil {
		if err == linebot.ErrInvalidSignature {
			w.WriteHeader(400)
		} else {
			w.WriteHeader(500)
		}
		return
	}

	for _, event := range events {
		log.Printf("Got event %v", event)
		switch event.Type {
		case linebot.EventTypeMessage:
			switch message := event.Message.(type) {
			case *linebot.TextMessage:
				if err := app.handleText(message, event.ReplyToken, event.Source); err != nil {
					log.Print(err)
				}
			case linebot.EventTypeFollow:
				if err := app.replyText(event.ReplyToken, "歡迎你加入!"); err != nil {
					log.Print(err)
				}
			/*case linebot.EventTypePostback:
				data := event.Postback.Data
				if data == "DATE" || data == "TIME" || data == "DATETIME" {
					data += fmt.Sprintf("(%v)", *event.Postback.Params)
				}
				if err := app.replyText(event.ReplyToken, "Got postback: "+data); err != nil {
					log.Print(err)
				}*/
			}
		}
	}
}

func (app *KitchenSink) replyText(replyToken, text string) error {
	if _, err := app.bot.ReplyMessage(
		replyToken,
		linebot.NewTextMessage(text),
	).Do(); err != nil {
		return err
	}
	return nil
}
